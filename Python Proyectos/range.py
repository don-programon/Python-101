# Listas - Tipo de dato
numeros = [5, 12, 10]

for n in numeros:
    print(n)

datos = ["tenis", 23, 5.345, "Jon"]

for d in datos:
    print(d)

#range - tipo de dato, FOR
#a = range(10)
for i in range(10, 20, 2):
    print(i)
    print("Rango del 10 al 20 saltando 2 numeros")

for i in range(20, 0, -2):
    print(i)
    print("Rango del 20 al 10 saltando 2 numeros regresivo")